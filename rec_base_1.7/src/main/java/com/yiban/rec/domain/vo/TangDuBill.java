package com.yiban.rec.domain.vo;

public class TangDuBill {

	private String tradeDate;
	private String tradeNo;
	private String orderId;
	private String tradeStatus;
	private String tradeBank;
	private String tradeFee;
	private String refundNo;
	private String merchantRefundNo;
	private String refundFee;
	private String refundStatus;
	private String orderDesc;
	private String remark;
	public TangDuBill() {
		super();
	}
	public String getTradeDate() {
		return tradeDate;
	}
	public void setTradeDate(String tradeDate) {
		this.tradeDate = tradeDate;
	}
	public String getTradeNo() {
		return tradeNo;
	}
	public void setTradeNo(String tradeNo) {
		this.tradeNo = tradeNo;
	}
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getTradeStatus() {
		return tradeStatus;
	}
	public void setTradeStatus(String tradeStatus) {
		this.tradeStatus = tradeStatus;
	}
	public String getTradeBank() {
		return tradeBank;
	}
	public void setTradeBank(String tradeBank) {
		this.tradeBank = tradeBank;
	}
	public String getTradeFee() {
		return tradeFee;
	}
	public void setTradeFee(String tradeFee) {
		this.tradeFee = tradeFee;
	}
	public String getRefundNo() {
		return refundNo;
	}
	public void setRefundNo(String refundNo) {
		this.refundNo = refundNo;
	}
	public String getMerchantRefundNo() {
		return merchantRefundNo;
	}
	public void setMerchantRefundNo(String merchantRefundNo) {
		this.merchantRefundNo = merchantRefundNo;
	}
	public String getRefundFee() {
		return refundFee;
	}
	public void setRefundFee(String refundFee) {
		this.refundFee = refundFee;
	}
	public String getRefundStatus() {
		return refundStatus;
	}
	public void setRefundStatus(String refundStatus) {
		this.refundStatus = refundStatus;
	}
	public String getOrderDesc() {
		return orderDesc;
	}
	public void setOrderDesc(String orderDesc) {
		this.orderDesc = orderDesc;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}

}
