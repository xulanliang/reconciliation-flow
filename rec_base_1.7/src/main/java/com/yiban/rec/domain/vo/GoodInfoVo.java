package com.yiban.rec.domain.vo;

public class GoodInfoVo {
	
	//商品信息
	private String Good_Info;
	
	//商品编码
	private String Good_No;
	
	//商品名称
	private String Good_Name;
	
	//商品描述
	private String Good_Desc;
	
	//商品价格
	private String Good_Price;
	
	//商品单位
	private String Good_Qty;

	//商品编码
	public String getGood_Info() {
		return Good_Info;
	}

	public void setGood_Info(String good_Info) {
		Good_Info = good_Info;
	}

	public String getGood_No() {
		return Good_No;
	}

	public void setGood_No(String good_No) {
		Good_No = good_No;
	}

	public String getGood_Name() {
		return Good_Name;
	}

	public void setGood_Name(String good_Name) {
		Good_Name = good_Name;
	}

	public String getGood_Desc() {
		return Good_Desc;
	}

	public void setGood_Desc(String good_Desc) {
		Good_Desc = good_Desc;
	}

	public String getGood_Price() {
		return Good_Price;
	}

	public void setGood_Price(String good_Price) {
		Good_Price = good_Price;
	}

	public String getGood_Qty() {
		return Good_Qty;
	}

	public void setGood_Qty(String good_Qty) {
		Good_Qty = good_Qty;
	}
	
	
}
